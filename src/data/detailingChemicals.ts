/**
 * Get chemicals by category
 */
export function getChemicalsByCategory(category: DetailingChemical['category']): DetailingChemical[] {
    return DETAILING_CHEMICALS.filter(c => c.category === category);
}

/**
 * Get all essential chemicals (bare minimum kit)
 */
export function getEssentialChemicals(): DetailingChemical[] {
    return DETAILING_CHEMICALS.filter(c => c.category.includes('essential'));
}

/**
 * Get all optional/upgrade chemicals
 */
export function getOptionalChemicals(): DetailingChemical[] {
    return DETAILING_CHEMICALS.filter(c => c.category.includes('optional'));
}

/**
 * Calculate total cost for a chemical kit
 */
export function calculateKitCost(chemicals: DetailingChemical[]): number {
    return chemicals.reduce((sum, chem) => sum + chem.suggestedPrice, 0);
}
